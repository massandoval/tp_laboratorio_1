#ifndef BIBLIOTECA_H_INCLUDED
#define BIBLIOTECA_H_INCLUDED



/** \brief Realiza una suma entre dos numeros.
 *
 * \param Numero en float/entero que ingreso el usuario.
 * \param Numero en float/entero que ingreso el usuario.
 * \return La suma de ambos numeros.
 *
 */
float hacerSuma (float numeroA, float numeroB);



/** \brief Realiza una resta entre dos numeros.
 *
 * \param Numero en float/entero que ingreso el usuario.
 * \param Numero en float/entero que ingreso el usuario.
 * \return La resta de ambos numeros.
 *
 */
float hacerResta (float numeroA, float numeroB);



/** \brief La multiplicacion de dos numeros.
 *
 * \param Numero en float/entero que ingreso el usuario.
 * \param Numero en float/entero que ingreso el usuario.
 * \return El producto de ambos numeros.
 *
 */
float hacerMultiplicacion (float numeroA, float numeroB);


/** \brief La division de dos numeros.
 *
 * \param Numero en float/entero que ingreso el usuario.
 * \param Numero en float/entero que ingreso el usuario.
 * \return El cociente de dos numeros.
 *
 */
float hacerDivision(float numeroA, float numeroB);


/** \brief El factorial de un numero (e).
 *
 * \param Numero entero.
 * \return Factorial de un entero.
 *
 */
int hacerFactorial (int numero);



#endif // BIBLIOTECA_H_INCLUDED
